def func(n,i,s):
    s1=[]#새로운 문자열을 저장하기위한 빈배열 생성
    for k in s:#배열에 있는 문자열을 *3해준다
        s1.append(k*3)
    for k in s:
        s1.append(k+(" "*i)+k)
    for k in s:#배열에 있는 문자열을 *3해준다
        s1.append(k*3)

    #n과 i가 같다면 끝이므로
    if(n==i):
        #출력
        for k in s:
            print(k)
        return 0
    func(n,i*3,s1)

#초기 리스트
s=["***","* *","***"]
#입력, 초기값, 초기 리스트
func(int(input()),3,s)